    private void sentToServer() throws IOException {
        UDPCliente client = new UDPCliente();
        String result = client.calculator(jtExpression.getText(), Integer.parseInt(jtClientPort.getText()), Integer.parseInt(jtServerPort.getText()), jtServerIP.getText());
        jtExpression.setText(jtExpression.getText() + " = " + result);
    }

